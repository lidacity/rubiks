def Shuffle(Count=42):
 random.seed()
 Prev = " "
 Step = 0
 while Step < Count:
  R = random.randint(0, len(Supported)-1)
  Command = Supported[R]
  if Prev != Command[0]:
   Prev = Command[0]
   Step += 1
